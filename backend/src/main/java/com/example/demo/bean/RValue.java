package com.example.demo.bean;

public class RValue {
    private String value;
    private String label;

    public void setValue(String v) {
        this.value = v;
    }

    public void setLabel(String l) {
        this.label = l;
    }

    public String getValue() {
        return this.value;
    }

    public String getLabel() {
        return this.label;
    }
}
