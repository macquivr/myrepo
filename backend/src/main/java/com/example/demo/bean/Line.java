package com.example.demo.bean;

public class Line {
    private String startValue;
    private String color;
    private String displayvalue;
    private String valueOnRight;
    private String thickness;

    public void setStartValue(String v) { startValue = v; }
    public String getStartValue() { return startValue; }

    public void setColor(String v) { color = v; }
    public String getColor() { return color; }

    public void setDisplayvalue(String v) { displayvalue = v; }
    public String getDisplayvalue() { return displayvalue; }

    public void setValueOnRight(String v) { valueOnRight = v; }
    public String getValueOnRight() { return valueOnRight; }

    public void setThickness(String v) { thickness = v; }
    public String getThickness() { return thickness; }
}
