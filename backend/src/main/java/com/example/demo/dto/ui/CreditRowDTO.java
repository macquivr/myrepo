package com.example.demo.dto.ui;

public class CreditRowDTO {
    private String label;
    private double usaa;
    private double amazon;
    private double capone;
    private double aaa;

    public String getLabel() { return label; }
    public double getUsaa() { return usaa; }
    public double getAmazon() { return amazon; }
    public double getCapone() { return capone; }
    public double getAaa() { return aaa; }

    public void setLabel(String s) { label = s; }
    public void setUsaa(double d) { usaa = d; }
    public void setAmazon(double d) { amazon = d; }
    public void setCapone(double d) { capone = d; }
    public void setAaa(double d) { aaa = d; }
}
